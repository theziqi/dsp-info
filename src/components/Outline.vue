      <div class="i-block">
        <div class="i-title">课程简介</div>
        <div class="i-content">
          <p class="text-indent">
            数字信号处理是现代信息处理和传输的基础课程之一，已经成为信号和信息处理、通信和电子、计算机科学和技术等专业的学生需要学习和掌握的基本知识。本课程以离散时间信号与系统分析作为对象，主要讨论数字信号处理的理论、原理与实现方法。在介绍经典理论的基础上，适当引入现代信号处理的理论与方法。主要内容包括离散时间系统、模拟信号的取样和量化、Z变换、离散时间傅立叶变换、离散傅立叶变换及其应用、快速傅立叶变换、IIR数字滤波器设计，FIR数字滤波器设计，数字滤波器实现，多速率信号简介等。
          </p>
          <p class="text-indent">
            本课程是进一步学习通信原理、数字通信、图像处理、无线通信、多媒体通信等专业课程的先修课程。
          </p>
        </div>
